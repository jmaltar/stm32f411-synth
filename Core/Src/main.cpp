static void midi_task(void) {
    uint8_t packet[4];
    while (tud_midi_available()) {
        if (!tud_midi_packet_read(packet)) break;

        uint8_t status = packet[1];
        uint8_t d1     = packet[2];
        uint8_t d2     = packet[3];

        char buf[80];
        switch (status & 0xF0) {
            case 0x90:
                __disable_irq();
                if (d2 == 0) synth.note_off(d1); else synth.note_on(d1, d2);
                __enable_irq();
                if (d2 > 0) {
                    auto& v = synth.voices[0];
                    snprintf(buf, sizeof(buf), "NoteOn: %d vel=%d | v0: note=%d freq=%.1f gain=%.3f idle=%d\r\n",
                        d1, d2, v.current_note, (double)v.freq, (double)v.gain, (int)v.adsr.idle);
                } else {
                    snprintf(buf, sizeof(buf), "NoteOff: %d\r\n", d1);
                }
                uart_print(buf);
                break;
            case 0x80:
                __disable_irq();
                synth.note_off(d1);
                __enable_irq();
                snprintf(buf, sizeof(buf), "NoteOff: %d\r\n", d1);
                uart_print(buf);
                break;
            default:
                snprintf(buf, sizeof(buf), "MIDI: %02X %02X %02X\r\n", status, d1, d2);
                uart_print(buf);
                break;
        }
    }
}
